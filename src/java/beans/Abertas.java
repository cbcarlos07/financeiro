public class Abertas {
    private String mes;
    private String contas;
    private String abertas;
    private String fechadas;
    private String nao_protoc;
    private String porc_protoc;
    private String abertas_rs; 

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getContas() {
        return contas;
    }

    public void setContas(String contas) {
        this.contas = contas;
    }

    public String getAbertas() {
        return abertas;
    }

    public void setAbertas(String abertas) {
        this.abertas = abertas;
    }

    public String getFechadas() {
        return fechadas;
    }

    public void setFechadas(String fechadas) {
        this.fechadas = fechadas;
    }

    public String getNao_protoc() {
        return nao_protoc;
    }

    public void setNao_protoc(String nao_protoc) {
        this.nao_protoc = nao_protoc;
    }

    public String getPorc_protoc() {
        return porc_protoc;
    }

    public void setPorc_protoc(String porc_protoc) {
        this.porc_protoc = porc_protoc;
    }

    public String getAbertas_rs() {
        return abertas_rs;
    }

    public void setAbertas_rs(String abertas_rs) {
        this.abertas_rs = abertas_rs;
    }
    
    
    
}
